package com.TradingCardSystem;

/**
 * Enum representing the different possible variants of a trading card.
 */
public enum Variant {
    /**
     * Default variant, indicating no special features.
     */
    NONE,

    /**
     * A standard print variant of the card.
     */
    NORMAL,

    /**
     * A card with extended artwork.
     */
    EXTENDED_ART,

    /**
     * A card featuring full artwork.
     */
    FULL_ART,

    /**
     * A card with alternate artwork.
     */
    ALT_ART
}
