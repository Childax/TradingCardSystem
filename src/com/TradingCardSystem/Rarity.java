package com.TradingCardSystem;

/**
 * The {@code Rarity} enum defines the rarity levels of a card in the trading card system.
 * These values can be used to determine the value and uniqueness of a card.
 */
public enum Rarity {
    /** A common card, typically easy to obtain. */
    COMMON,

    /** An uncommon card, slightly harder to find than common cards. */
    UNCOMMON,

    /** A rare card, valuable and less frequently encountered. */
    RARE,

    /** A legendary card, the most ifficult to obtain. */
    LEGENDARY
}
